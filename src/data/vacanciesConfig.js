export function calculateSalary(positionKey, durationMonths = 3, multiplier = 1.0) {
  const salaryItem = SALARY_DATA[positionKey];
  if (!salaryItem) return null;
  const monthlySalary = salaryItem.base * multiplier;
  const totalSalary = monthlySalary * durationMonths;
  const taxes = totalSalary * TAX_RATE;
  const netSalary = totalSalary - taxes;
  const grossIncome = UPLIFTS + totalSalary;
  const netIncome = UPLIFTS + netSalary;
  return { position: salaryItem.label, monthlySalary, totalSalary, taxes, netSalary, grossIncome, netIncome, uplifts: UPLIFTS };
}

export function calculateVacancySalary(vacancyId, level = "min", driverCategory = null) {
  const vacancy = VACANCIES.find((v) => v.id === vacancyId);
  if (!vacancy) return null;

  let monthlyBase;
  if (vacancy.salary.type === "by-category" && driverCategory) {
    const cat = vacancy.salary.categories[driverCategory];
    if (!cat) return null;
    monthlyBase = level === "max" ? cat.max : cat.min;
  } else {
    monthlyBase = level === "max" ? vacancy.salary.max : vacancy.salary.min;
  }

  const stintDuration = GLOBAL_CONFIG.compensation.stintDuration;
  const totalForMonths = monthlyBase * stintDuration;
  const oneTimePayment = UPLIFTS;
  const totalForStint = totalForMonths + oneTimePayment;

  return {
    stintDuration,
    monthlySalary: monthlyBase,
    monthlySalaryFormatted: formatCurrency(monthlyBase),
    totalMonths: totalForMonths,
    totalMonthsFormatted: formatCurrency(totalForMonths),
    oneTimePayment,
    oneTimePaymentFormatted: formatCurrency(oneTimePayment),
    totalForStint,
    totalForStintFormatted: formatCurrency(totalForStint),
  };
}

export function calculateVacancySalaryWithTax(vacancyId, level = "min", driverCategory = null) {
  const vacancy = VACANCIES.find((v) => v.id === vacancyId);
  if (!vacancy) return null;

  const { stintDuration, oneTimePayment } = GLOBAL_CONFIG.compensation;

  let monthlySalary;
  if (vacancy.salary.type === "by-category" && driverCategory) {
    const cat = vacancy.salary.categories[driverCategory];
    if (!cat) return null;
    monthlySalary = level === "max" ? cat.max : cat.min;
  } else {
    monthlySalary = level === "max" ? vacancy.salary.max : vacancy.salary.min;
  }

  const taxOnSalary = Math.round(monthlySalary * TAX_RATE);
  const netMonthlySalary = monthlySalary - taxOnSalary;
  const totalGrossSalary = monthlySalary * stintDuration;
  const totalTaxOnSalary = taxOnSalary * stintDuration;
  const totalNetSalary = totalGrossSalary - totalTaxOnSalary;
  const totalNetWithBonus = totalNetSalary + oneTimePayment;
  const totalGrossWithBonus = totalGrossSalary + oneTimePayment;

  return {
    monthlySalaryGross: monthlySalary,
    monthlySalaryGrossFormatted: formatCurrency(monthlySalary),
    monthlyTax: taxOnSalary,
    monthlyTaxFormatted: formatCurrency(taxOnSalary),
    monthlySalaryNet: netMonthlySalary,
    monthlySalaryNetFormatted: formatCurrency(netMonthlySalary),
    totalGrossSalary,
    totalGrossSalaryFormatted: formatCurrency(totalGrossSalary),
    totalTaxOnSalary,
    totalTaxOnSalaryFormatted: formatCurrency(totalTaxOnSalary),
    totalNetSalary,
    totalNetSalaryFormatted: formatCurrency(totalNetSalary),
    oneTimePayment,
    oneTimePaymentFormatted: formatCurrency(oneTimePayment),
    totalNetWithBonus,
    totalNetWithBonusFormatted: formatCurrency(totalNetWithBonus),
    totalGrossWithBonus,
    totalGrossWithBonusFormatted: formatCurrency(totalGrossWithBonus),
    stintDuration,
    taxRate: TAX_RATE * 100,
  };
}

