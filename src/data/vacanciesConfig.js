const ICON_MAP = {
  Строительство: "HardHat",
  Транспорт: "Truck",
  Медицина: "Stethoscope",
  Безопасность: "Shield",
};

function buildSalaryData() {
  const data = {};
  for (const v of VACANCIES) {
    if (v.salary.type === "by-category") {
      for (const [, catData] of Object.entries(v.salary.categories)) {
        data[catData.calcKey] = {
          label: `${v.position} кат. ${catData.name.replace("Категория ", "")}`,
          base: catData.base,
          category: v.category,
          icon: ICON_MAP[v.category] || "Briefcase",
        };
      }
    } else {
      data[v.id] = {
        label: v.position,
        base: v.salary.calcBase,
        category: v.category,
        icon: ICON_MAP[v.category] || "Briefcase",
      };
    }
  }
  return data;
}

export const SALARY_DATA = buildSalaryData();
export const SALARY_LIST = Object.entries(SALARY_DATA).map(([key, val]) => ({ key, ...val }));

